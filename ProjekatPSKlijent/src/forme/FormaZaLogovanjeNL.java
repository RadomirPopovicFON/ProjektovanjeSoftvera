    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user = jUser.getText();
        String sifra = jSifra.getText();
        NadleznoLice nl = new NadleznoLice();
        nl.setKorisnickoIme(user);
        nl.setKorisnickaSifra(sifra);
        try {
            
            TransferObjekatZahtev toZahtev = new TransferObjekatZahtev();
            toZahtev.setOperacija(Konstante.VRATI_NL);
            toZahtev.setParametar(nl);
            KomunikacijaKlijent.getInstance().posaljiZahtev(toZahtev);
            
            
            TransferObjekatOdgovor odgovor = (TransferObjekatOdgovor) KomunikacijaKlijent.getInstance().primiOdgovor();
            
            OpstiDomenskiObjekat n = (OpstiDomenskiObjekat)odgovor.getRezultat();
            
            if(n!=null){
                JOptionPane.showMessageDialog(this, "Ulogovali ste se pod imenom "+((NadleznoLice)n).getIme());
                
                FrmUnosStudenta frm = new FrmUnosStudenta((NadleznoLice)n);
                frm.setVisible(true);
                this.dispose();
            }
            else JOptionPane.showMessageDialog(this, "Nema korisnika pod tim usernameom/sifrom");
            
        }
            catch (Exception ex) {
            Logger.getLogger(FormaZaLogovanjeNL.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Neuspesno logovanje!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


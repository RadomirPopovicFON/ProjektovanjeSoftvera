    private void pripremiPanel() {
        broj=0;
        jComboPrijavljeni.removeAllItems();
        jComboNeprijavljeni.removeAllItems();
        jComboPredmet.removeAllItems();

        pripremiPanelPredmetiPoGodini();
        
        try {
            
            TransferObjekatZahtev toZahtev1 = new TransferObjekatZahtev();
            toZahtev1.setOperacija(Konstante.VRATI_IZ_KOLEKCIJE_PREDMETE);
            
            KomunikacijaKlijent.getInstance().posaljiZahtev(toZahtev1);
            
            TransferObjekatOdgovor odgovor1 = (TransferObjekatOdgovor) KomunikacijaKlijent.getInstance().primiOdgovor();
            
            List<OpstiDomenskiObjekat> predmetiIzKolekcije = (List<OpstiDomenskiObjekat>)odgovor1.getRezultat();
            
            
            TransferObjekatZahtev toZahtev2 = new TransferObjekatZahtev();
            toZahtev2.setOperacija(Konstante.VRATI_PREDMETE);
            toZahtev2.setParametar(s);
            
            KomunikacijaKlijent.getInstance().posaljiZahtev(toZahtev2);
            
            TransferObjekatOdgovor odgovor2 = (TransferObjekatOdgovor) KomunikacijaKlijent.getInstance().primiOdgovor();
            
            List<OpstiDomenskiObjekat> mojiPredmeti = (List<OpstiDomenskiObjekat>)odgovor2.getRezultat();
            
            
            for(OpstiDomenskiObjekat p:mojiPredmeti){
                broj+=(((StudentPredmet)p).getP()).getBrojESPB();
                jComboPrijavljeni.addItem((StudentPredmet)p);
                    for(OpstiDomenskiObjekat o : predmetiIzKolekcije){
                    if(!(((StudentPredmet)p).getP()).getNazivPredmeta().equals((((Predmet)o)).getNazivPredmeta()))
                    {
                        jComboNeprijavljeni.addItem(((Predmet)o));
                    }
                }
            }
            jBrojESPB.setText(broj+"");
        } catch (Exception ex) {
            Logger.getLogger(PrijavaPredmetaZaSlusanjePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    private void pripremiPanelGodina(){
        jComboGodina.removeAllItems();
        jComboPredmet.removeAllItems();
        
        jComboPrijavljeni.removeAllItems();
        jComboNeprijavljeni.removeAllItems();
        for (int i=1;i<5;i++) {
            jComboGodina.addItem(i);
        }
    }
    private void pripremiPanelPredmetiPoGodini(){
        try {
            jComboPredmet.removeAllItems();
            
            
            
            TransferObjekatZahtev toZahtev1 = new TransferObjekatZahtev();
            toZahtev1.setOperacija(Konstante.VRATI_IZ_KOLEKCIJE_PREDMETE);
            KomunikacijaKlijent.getInstance().posaljiZahtev(toZahtev1);
            TransferObjekatOdgovor odgovor1 = (TransferObjekatOdgovor) KomunikacijaKlijent.getInstance().primiOdgovor();
            
            List<OpstiDomenskiObjekat> predmetiIzKolekcije = (List<OpstiDomenskiObjekat>)odgovor1.getRezultat();
            
            
            for(OpstiDomenskiObjekat ob : predmetiIzKolekcije){
                jComboPredmet.addItem((Predmet)ob);
            }
        } catch (IOException ex) {
            Logger.getLogger(PrijavaPredmetaZaSlusanjePanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PrijavaPredmetaZaSlusanjePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }


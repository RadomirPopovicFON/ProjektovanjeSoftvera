    private void jPrijaviPredmetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPrijaviPredmetActionPerformed
        Predmet p = (Predmet)jComboPredmet.getSelectedItem();
        System.out.println("Izabran : "+p.getPredmetID());
        
        StudentPredmet sp = new StudentPredmet(s,p);
        
        try {
            TransferObjekatZahtev toZahtev = new TransferObjekatZahtev();
            toZahtev.setOperacija(Konstante.SACUVAJ_PREDMET);
            toZahtev.setParametar(sp);
            
            KomunikacijaKlijent.getInstance().posaljiZahtev(toZahtev);
            
            TransferObjekatOdgovor odgovor = (TransferObjekatOdgovor) KomunikacijaKlijent.getInstance().primiOdgovor();
            String poruka = (String) odgovor.getPoruka();
            
            if(poruka.equals("Predmet je uspesno sacuvan.")){
            Object[] options = {"NE",
                                "DA"};
                int odg = JOptionPane.showOptionDialog(this,
                "Uspesno ste uneli novi predmet za slusanje! Zelite li da nastavite sa radom?",
                "Izlazak",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,     //do not use a custom Icon
                options,  //the titles of buttons
                options[0]);
                if (odg == JOptionPane.YES_OPTION){
                    SwingUtilities.getWindowAncestor(this).dispose();
                }else{
                    pripremiPanel();
                }
            }
            else JOptionPane.showMessageDialog(this, odgovor.getIzuzetak().toString());
            
        } catch (Exception ex) {
            Logger.getLogger(PrijavaPredmetaZaSlusanjePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jPrijaviPredmetActionPerformed

